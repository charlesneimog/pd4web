// ────────── User Data Struct ──────────
enum Pd4WebSenderType {
    BANG = 0,
    FLOAT,
    SYMBOL,
    LIST,
    MESSAGE,
    MOUSE_EVENT,
    KEY_EVENT,
    TOUCH_EVENT
};

// Thread-safe atom structure for passing list/message data
struct Pd4WebAtom {
    enum Type { FLOAT_TYPE, SYMBOL_TYPE };
    Type type = FLOAT_TYPE;
    float f_value = 0.0f;
    char s_value[128]{};
};

// Event data structures
struct MouseEventData {
    int x;
    int y;
    int button;
    bool shift;
    bool ctrl;
    bool alt;
    enum EventType { MOUSE_DOWN, MOUSE_UP, MOUSE_MOVE } event_type;
};

struct KeyEventData {
    char key[64]{};
    int keyCode;
    bool shift;
    bool ctrl;
    bool alt;
    enum EventType { KEY_DOWN, KEY_UP } event_type;
};

struct TouchEventData {
    int x;
    int y;
    int identifier;
    enum EventType { TOUCH_START, TOUCH_END, TOUCH_MOVE, TOUCH_CANCEL } event_type;
};

struct PendingFile {
    std::string filename;
    std::vector<uint8_t> buffer;
};

struct Pd4WebUserData {
    class Pd4Web *pd4web;
    t_pdinstance *libpd;

    bool redraw = true;
    bool needsSwap = false;
    int fbWidth;
    int fbHeight;

    // Sound state
    bool soundInit;
    bool soundSuspended;
    std::string soundToggleSel;

    // last frame
    double lastFrame;

    // Canvas selection
    std::string canvasSel;
    std::string canvasId;

    // Audio processing synchronization
    std::mutex mtx;
    std::condition_variable cv;
    bool drawing = false;
    bool processing = false;

    // Mouse state
    bool mousedown;
    bool doit;
    int xpos;
    int ypos;
    int canvas_width;
    int canvas_height;
    int canvas_marginx;
    int canvas_marginy;
    float devicePixelRatio;
    t_gobj *obj = nullptr; // Object captured by the current mouse gesture (audio thread only)
    t_canvas *canvas;

    // Keyboard input
    std::string key;

    // Main-runtime-thread-only renderer. The AudioWorklet only writes RenderTransport slots.
    std::unique_ptr<ThorVGRenderer> renderer;
    bool openNumberKeyboard = false;

    // Files
    std::queue<PendingFile> pendingFiles;
    std::mutex pendingFilesMutex; // if threads can access simultaneously
};

// ─────── Senders and Receivers ────
// Thread-safe sender structure with owned string data
struct Pd4WebSender {
    static constexpr uint16_t MaxAtoms = 64;
    Pd4WebSenderType type = BANG;
    char receiver[128]{};
    char selector[128]{};
    float f_value = 0;
    char s_value[256]{};
    uint16_t atomCount = 0;
    std::array<Pd4WebAtom, MaxAtoms> list_data{};

    // Event data
    MouseEventData mouse_data;
    KeyEventData key_data;
    TouchEventData touch_data;

};

static std::unordered_map<t_pdinstance *, std::unordered_map<std::string, emscripten::val>>
    ReceiverListeners;

using ReceiverMap = std::unordered_map<std::string, emscripten::val>;
using Pd4WebReceiverListeners = std::unordered_map<t_pdinstance *, ReceiverMap>;

static Pd4WebReceiverListeners BangReceiverListeners;
static Pd4WebReceiverListeners FloatReceiverListeners;
static Pd4WebReceiverListeners SymbolReceiverListeners;
static Pd4WebReceiverListeners ListReceiverListeners;
static Pd4WebReceiverListeners MessageReceiverListeners;

// ╭─────────────────────────────────────╮
// │             Main Class              │
// ╰─────────────────────────────────────╯
class Pd4Web {
  public:
    ~Pd4Web() {
        if (m_UserData && m_UserData->renderer) m_UserData->renderer.reset();
        for (auto symbol : m_BindSymbols) {
            libpd_unbind(symbol);
        }
        libpd_free_instance(m_PdInstance);
    }

    // Main
    void Init();
    void ToggleAudio();
    void OpenPatchJS(const std::string &patchPath, emscripten::val options);

    // Send messages
    bool SendFloat(std::string receiver, float value);
    bool SendSymbol(std::string receiver, std::string symbol);
    bool SendBang(std::string receiver);
    bool SendList(std::string receiver, emscripten::val list);
    bool SendMessage(std::string receiver, std::string msg, emscripten::val list);

    // Bind callbacks
    void OnBangReceived(std::string receiver, emscripten::val callback);
    void OnFloatReceived(std::string receiver, emscripten::val callback);
    void OnSymbolReceived(std::string receiver, emscripten::val callback);
    void OnListReceived(std::string receiver, emscripten::val callback);
    void OnMessageReceived(std::string r, emscripten::val callback);

    // Mouse position
    void GetLastMousePosition(int *x, int *y);
    void SetLastMousePosition(int x, int y);

    // File Transfering
    bool SendFile(emscripten::val jsArrayBuffer, std::string filename);

    // WebAudio
    EMSCRIPTEN_WEBAUDIO_T GetWebAudioContext();
    void SetWebAudioContext(EMSCRIPTEN_WEBAUDIO_T context);
    void SetSampleRate(float sampleRate);
    float GetSampleRate();

    // Get
    std::string GetPatchPath();
    std::string GetCanvasId();
    std::string GetSoundToggleId();
    std::string GetProjectName();
    int GetChannelCountIn();
    int GetChannelCountOut();
    float GetPatchZoom();
    int GetFps();
    std::string GetBGColor();
    std::string GetFGColor();

    bool RenderGui();
    bool UseMidi();
    bool EnqueueSender(const Pd4WebSender &sender) noexcept;
    const Pd4WebSender *BeginSender() const noexcept { return m_ToSendQueue.beginPop(); }
    void EndSender() noexcept { m_ToSendQueue.commitPop(); }
    uint64_t DroppedSenders() const noexcept { return m_DroppedSenders.load(); }

  private:
    void OpenPatch(std::string patchPath, std::string patchCanvasId, std::string soundToggleId);
    void StartMidi();

    std::vector<void *> m_BindSymbols;
    RenderSpscQueue<Pd4WebSender, 257> m_ToSendQueue;
    std::atomic<uint64_t> m_DroppedSenders{0};

    // Ticks
    int m_MidiTickID;

    // Theme
    std::string m_BgColor;
    std::string m_FgColor;
    bool m_SoundIdWarning;

    // User data
    std::shared_ptr<Pd4WebUserData> m_UserData;
    bool m_Pd4WebAudioWorkletInit = false;
    bool m_AudioSuspended = false;
    t_pdinstance *m_PdInstance = nullptr;
    EMSCRIPTEN_WEBAUDIO_T m_AudioContext;

    // Mouse
    int m_LastMouseX = 0;
    int m_LastMouseY = 0;

    // User Configurations
    std::string m_PatchPath = "";
    std::string m_CanvasId = "";
    std::string m_SoundToggleId = "";
    std::string m_ProjectName = "";

    int m_ChannelCountIn = 0;
    int m_ChannelCountOut = 0;
    float m_SampleRate = 48000.0f;
    bool m_UseMidi;

    float m_PatchZoom = 1;
    bool m_RenderGui = true;
    int m_Fps = 0;
};

